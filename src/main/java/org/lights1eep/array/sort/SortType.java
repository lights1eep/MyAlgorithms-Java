package org.lights1eep.array.sort;

/**
 * 排序类型
 * @author lights1eep
 */
public enum SortType {
    /**
     * 冒泡排序
     */
    BUBBLE,
    /**
     * 选择排序
     */
    SELECTION,
    /**
     * 插入排序
     */
    INSERTION,
    /**
     * 希尔排序
     */
    SHELL,
    /**
     * 归并排序 迭代
     */
    MERGE_ITERATION,
    /**
     * 归并排序 递归
     */
    MERGE_RECURSION,
    /**
     * 快速排序
     */
    QUICK,
    /**
     * 堆排序
     */
    HEAP,
    /**
     * 计数排序
     */
    COUNTING,
    /**
     * 桶排序
     */
    BUCKET,
    /**
     * 基数排序
     */
    RADIX
}
